#include <bits/stdc++.h>

using namespace std;

int XorSubsets(vector<int>& vec,int n)
{
	if(n==1)
		return vec[0];
	else
		return 0;
}

int main()
{
	int testcases;
	cin>>testcases;
	while(testcases)
	{
		int n;
		cin>>n;
		vector<int> vec(n);
		for(int i=0;i<n;i++)
			cin>>vec[i];
		cout<<XorSubsets(vec,n)<<endl;
		testcases--;
	}
	return 0;
}
